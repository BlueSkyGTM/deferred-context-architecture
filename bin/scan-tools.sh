#!/usr/bin/env bash
# scan-tools.sh — the EXECUTABLE Tool scan (universal tools + any active silo's tools).
# Runs the universal manifest's detect tests, reports PRESENT / MISSING / MISSING-ASK / PARTIAL,
# and regenerates tool-status.md. On ANY clone, run this first to learn what to install:
#     bash bin/scan-tools.sh
# Universal detects are below; a silo may declare its own tooling.md, scanned into its folder.
set -u
ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
OUT="$ROOT/tool-status.md"
miss_required=0
rows=""
add() { rows="${rows}| $1 | $2 | $3 | $4 |\n"; }            # name | required | status | note
have() { eval "$1" >/dev/null 2>&1; }                       # run a detect test quietly

# --- universal manifest detects (keep in sync with _core/tooling.md) ---
if have '[ -d ~/.claude/skills/gstack ]'; then add "gstack skills" yes PRESENT "harness + standing skills";
  else add "gstack skills" yes MISSING "ships with the Claude Code env"; miss_required=1; fi
if have '[ -d ~/.claude/skills/gstack ]'; then add "context-compressor / memory-manager" yes PRESENT "session-boundary compression + durable memory (part of gstack)";
  else add "context-compressor / memory-manager" yes MISSING "part of gstack (install gstack)"; miss_required=1; fi
if have 'command -v codex'; then add "codex" no PRESENT "cross-model review";
  else add "codex" no MISSING "npm i -g @openai/codex"; fi
if have 'command -v bun'; then add "bun" "if-gbrain" PRESENT "JS runtime the gbrain CLI runs on";
  else add "bun" "if-gbrain" MISSING "irm bun.sh/install.ps1 | iex (Win) / curl -fsSL https://bun.sh/install | bash (unix)"; fi
if have 'command -v gbrain && [ -f ~/.gbrain/config.json ]'; then add "gbrain" no PRESENT "retrieval (optional; needs OPENAI_API_KEY)";
  else add "gbrain" no MISSING "clone+link github.com/garrytan/gbrain (NOT npm i -g gbrain: wrong package)"; fi
if have '[ -f "$ROOT/.gbrain-source" ]'; then add "gbrain repo-pin" no PRESENT "this repo indexed";
  else add "gbrain repo-pin" no MISSING-ASK "/sync-gbrain --full (needs human)"; fi
if have '[ -d ~/.claude/plugins/marketplaces/ponytail ]'; then add "ponytail (plugin)" no PARTIAL "marketplace added; run /plugin install ponytail@ponytail to enable. CODING scope only.";
  else add "ponytail (plugin)" no MISSING-ASK "/plugin marketplace add DietrichGebert/ponytail"; fi
if have 'python -c "import llmlingua"'; then add "LLMLingua" no PRESENT "content-side token reduction (compress context)";
  else add "LLMLingua" no MISSING "pip install llmlingua"; fi
if have 'python -c "import markitdown"'; then add "markitdown" "if-pdf/office" PRESENT "intake -> markdown";
  else add "markitdown" "if-pdf/office" MISSING "pip install markitdown"; fi
if have 'python -c "import youtube_transcript_api"'; then add "youtube-transcript" "if-video" PRESENT "intake";
  else add "youtube-transcript" "if-video" MISSING "pip install youtube-transcript-api"; fi
if have 'python -c "import trafilatura"'; then add "article-extractor" "if-web" PRESENT "intake (trafilatura)";
  else add "article-extractor" "if-web" MISSING-ASK "pip install trafilatura (confirm)"; fi
if have 'command -v mmdc'; then add "mermaid (mmdc)" "if-visual" PRESENT "rasterize mermaid to svg/png; inline mermaid in markdown needs no tool";
  else add "mermaid (mmdc)" "if-visual" MISSING "npm i -g @mermaid-js/mermaid-cli (only to rasterize; inline mermaid needs nothing)"; fi
add "excalidraw (format)" "if-visual" PRESENT "agent writes .excalidraw JSON; no install to produce (view at excalidraw.com or VS Code ext)"
add "evaluator" yes PRESENT "method (rubric + fresh-context pass); no external tool"
if have 'command -v claude'; then add "claude (Claude Code CLI)" yes PRESENT "the domain runtime; live dry-run harness";
  else add "claude (Claude Code CLI)" yes MISSING-ASK "install per official Claude Code docs (auth is machine-specific)"; miss_required=1; fi
if have '[ -d ~/.claude/plugins/marketplaces/claude-plugins-official/plugins/skill-creator ]'; then add "skill-creator / skill-auditor" "if-skills" PRESENT "skill authoring + audit (claude-plugins-official)";
  else add "skill-creator / skill-auditor" "if-skills" MISSING-ASK "/plugin install skill-creator@claude-plugins-official (interactive)"; fi

{
  echo "# tool-status.md — generated by bin/scan-tools.sh"
  echo "# scan: $(date -u +%Y-%m-%dT%H:%M:%SZ) | host: $(uname -s 2>/dev/null || echo unknown)"
  echo "# Per-machine, regenerated each scan. Do NOT hand-edit or commit (gitignored)."
  echo "# Source of truth for WHAT is expected: the universal manifest in _core/tooling.md."
  echo
  echo "| tool | required | status | note |"
  echo "|---|---|---|---|"
  printf "%b" "$rows"
  echo
  if [ "$miss_required" = "1" ]; then
    echo "RESULT: a REQUIRED tool is MISSING — a run is BLOCKED until it is installed (see notes)."
  else
    echo "RESULT: all REQUIRED universal tools present. Install conditional/optional tools as a run needs them; resolve MISSING-ASK with the human."
  fi
} > "$OUT"

# --- active-silo manifest scan (also scan silos/<name>/tooling.md -> silos/<name>/tool-status.md) ---
for pdir in "$ROOT"/silos/*/; do
  [ -d "$pdir" ] || continue
  pname="$(basename "$pdir")"
  [ -f "${pdir}tooling.md" ] || continue
  prows=""; pmiss=0
  while IFS= read -r line; do
    case "$line" in '|'*'|') ;; *) continue ;; esac                                   # table rows only
    printf '%s' "$line" | grep -qiE '^\|[[:space:]]*tool[[:space:]]*\|' && continue    # skip header
    printf '%s' "$line" | grep -qE  '^\|[[:space:]]*:?-+' && continue                  # skip separator
    ptool="$(printf '%s' "$line" | awk -F'|' '{print $2}' | sed 's/^ *//;s/ *$//')"
    preq="$(printf  '%s' "$line" | awk -F'|' '{print $4}' | sed 's/^ *//;s/ *$//')"
    pdet="$(printf  '%s' "$line" | awk -F'|' '{print $5}' | sed 's/^ *//;s/ *$//;s/^`//;s/`$//')"
    case "$pdet" in *'<'*'>'*|'') continue ;; esac                                     # skip unfilled/example rows
    if have "$pdet"; then prows="${prows}| ${ptool} | ${preq} | PRESENT | silo |\n";
      else prows="${prows}| ${ptool} | ${preq} | MISSING | silo |\n"; [ "$preq" = "yes" ] && pmiss=1; fi
  done < "${pdir}tooling.md"
  {
    echo "# silos/${pname}/tool-status.md — generated by bin/scan-tools.sh ($(date -u +%Y-%m-%dT%H:%M:%SZ))"
    echo "# Per-machine, gitignored. SILO tools for silo '${pname}'. Universal tools: see root tool-status.md."
    echo
    echo "| tool | required | status | note |"
    echo "|---|---|---|---|"
    printf "%b" "${prows:-| (none) | - | - | no runnable silo detects in silos/${pname}/tooling.md |\n}"
  } > "${pdir}tool-status.md"
  [ "$pmiss" = "1" ] && miss_required=1
  echo "scanned active silo '${pname}' -> silos/${pname}/tool-status.md"
done

cat "$OUT"
exit "$miss_required"
